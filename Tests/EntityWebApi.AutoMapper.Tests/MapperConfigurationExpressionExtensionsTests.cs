        [Test]
        public void CreateEntityMaps_ChildsUpdated()
        {
            var dataContext = new DataContext(_dbContextOptions);
            var typeStore = new TypeStore();
            typeStore.Add(typeof(ItemDto));
            typeStore.Add(typeof(ItemWithChildsDto));
            var dto = new ItemWithChildsDto
            {
                Id = 2,
                Value = 3,
                Items = new[]
                {
                    new ItemDto { Id = 10, Value = 1 },
                    new ItemDto { Id = 11, Value = 12 }
                }
            };
            var item = new ItemWithChilds
            {
                Id = 2,
                Value = 3,
                Items = new Collection<Item>
                {
                    new Item { Id = 10, Value = 11 },
                    new Item { Id = 12, Value = 13 }
                }
            };

            var mapper = new MapperConfiguration(
                config =>
                {
                    config.AddEntityFrameworkCoreCollectionMappers(dataContext);
                    config.CreateEntityMaps(dataContext, typeStore, "Dto");
                }).CreateMapper();
            mapper.Map(dto, item);

            item.Id.ShouldBe(2);
            item.Value.ShouldBe(3);
            item.Items.Count.ShouldBe(2);
            item.Items.Any(i => i.Id == 10 && i.Value == 1).ShouldBeTrue();
            item.Items.Any(i => i.Id == 0 && i.Value == 12).ShouldBeTrue();
        }

        [Test]
        public async Task CreateEntityMaps_ChildsUpdatedInDataContext()
        {
            var dataContext = new DataContext(_dbContextOptions);
            var typeStore = new TypeStore();
            typeStore.Add(typeof(ItemDto));
            typeStore.Add(typeof(ItemWithChildsDto));
            var dto = new ItemWithChildsDto
            {
                Id = 2,
                Value = 3,
                Items = new[]
                {
                    new ItemDto { Id = 10, Value = 1 },
                    new ItemDto { Id = 11, Value = 12 }
                }
            };
            var item = new ItemWithChilds
            {
                Id = 2,
                Value = 3,
                Items = new Collection<Item>
                {
                    new Item { Id = 10, Value = 11 },
                    new Item { Id = 12, Value = 13 }
                }
            };
            await dataContext.AddAsync(item);
            await dataContext.SaveChangesAsync();

            var mapper = new MapperConfiguration(
                config =>
                {
                    config.AddEntityFrameworkCoreCollectionMappers(dataContext);
                    config.CreateEntityMaps(dataContext, typeStore, "Dto");
                }).CreateMapper();
            mapper.Map(dto, item);
            await dataContext.SaveChangesAsync();

            var savedItem = dataContext.ItemsWithChilds
                .AsNoTracking()
                .Include(c => c.Items)
                .Single(item => item.Id == 2);
            savedItem.Id.ShouldBe(2);
            savedItem.Value.ShouldBe(3);
            savedItem.Items.Count.ShouldBe(2);
            savedItem.Items.Any(i => i.Id == 10 && i.Value == 1).ShouldBeTrue();
            savedItem.Items.Any(i => i.Id != 12 && i.Value == 12).ShouldBeTrue();
        }

