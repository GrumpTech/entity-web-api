        {
            var dataContext = new DataContext(_dbContextOptions);
            var typeStore = new TypeStore();
            typeStore.Add(typeof(NoKeyItemDto));
            var item = new NoKeyItem { Value = 2 };

            var mapper = new MapperConfiguration(
                config => config.CreateEntityMaps(dataContext, typeStore, "Dto"))
                .CreateMapper();
            var mapped = mapper.MapNotNull<NoKeyItemDto>(item);
            var mappedBack = mapper.MapNotNull<NoKeyItem>(mapped);

            mapped.Value.ShouldBe(2);
            mappedBack.Value.ShouldBe(2);
        }

